@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly dicts: Record<Lang, Dict> = { ru: RU, en: EN };
  readonly lang = signal<Lang>(this.readLang());

  private readLang(): Lang {
    const saved = (typeof localStorage !== 'undefined' && localStorage.getItem('cmx.lang')) as Lang | null;
    return saved === 'en' || saved === 'ru' ? saved : 'ru';
  }

  setLang(lang: Lang) {
    this.lang.set(lang);
    if (typeof localStorage !== 'undefined') localStorage.setItem('cmx.lang', lang);
  }
  toggle() {
    this.setLang(this.lang() === 'ru' ? 'en' : 'ru');
  }

  t(key: string): string {
    return this.dicts[this.lang()][key] ?? this.dicts.en[key] ?? key;
  }
}
